//Ciclo while
var contador = 10;

while (contador>=1)
{
    console.log("Voy por el numero:",contador);
    contador = contador -1;
}

//Ciclo for
for(var i= 10; i>=1; i--)
{
    console.log("Voy por el numero:",i);
}

